# 5й урок. Арифметические операторы
print(2 + 3) # 5
print(10 - 3) # 7
print(10 * 3) # 30
print(10 / 2) # 5.0
print(30 % 2) # 0
print(3 // 2) # 1
print(7 % 2) # 1
print(7 // 2) # 3
print(-5 - 3) # -8
print(3 ** 2) # 9
print(27 ** (1 / 3)) # 3
print(2 + 3 * 4) # 14
print((2 + 3) * 4) # 20
print(0.5 + 1.5) # 2
print(.5 + 2) # 2.5
